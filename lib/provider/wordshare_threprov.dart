class WordProvider3 extends ChangeNotifier {
  List<Words3> initialList3 = [];
  int lastIndex = 0;

  Future<void> _loadLastIndex() async {
    final prefs = await SharedPreferences.getInstance();
    lastIndex = prefs.getInt('lastIndex') ?? 0;
    notifyListeners(); // Notify listeners about the loaded index
  }

  Future<void> _saveLastIndex() async {
    final prefs = await SharedPreferences.getInstance();
    prefs.setInt('lastIndex', lastIndex);
  }

  void setLastIndex(int index) {
    lastIndex = index;
    _saveLastIndex();
    notifyListeners(); // Notify listeners about the updated index
  }

  WordProvider3() {
    loadData3();
    _loadLastIndex();
    wordsListThre.shuffle();
    initialList3.shuffle();
    initialList3.addAll(wordsListThre);
  }

  void loadData3() async {
    SharedPreferences prefs = await SharedPreferences.getInstance();

    List<String>? questList3 = prefs.getStringList('questList3');
    List<String>? answerList3 = prefs.getStringList('answerList3');
    List<String>? backList3 = prefs.getStringList('backList3');
    List<String>? frontList3 = prefs.getStringList('frontList3');

    wordsListThre.clear(); // Mevcut listeyi temizle

    if (questList3 != null &&
        answerList3 != null &&
        backList3 != null &&
        frontList3 != null) {
      for (int i = 0; i < questList3.length; i++) {
        Words3 word = Words3(
          list: 'B1',
          answer: answerList3[i],
          quest: questList3[i],
          back: backList3[i],
          front: frontList3[i],
        );
        wordsListThre.add(word);
      }
    }

    notifyListeners();
  }

  void saveData3() async {
    SharedPreferences prefs = await SharedPreferences.getInstance();
    List<String> questList3 = [];
    List<String> answerList3 = [];
    List<String> backList3 = [];
    List<String> frontList3 = [];

    for (Words3 word3 in wordsListThre) {
      questList3.add(word3.quest);
      answerList3.add(word3.answer);
      frontList3.add(word3.front);
      backList3.add(word3.back);
    }

    prefs.setStringList('questList3', questList3);
    prefs.setStringList('answerList3', answerList3);
    prefs.setStringList('backList3', backList3);
    prefs.setStringList('frontList3', frontList3);
    notifyListeners();
  }

  void deleteWord3(int index, BuildContext context) {
    if (wordsListThre.isNotEmpty) {
      wordsListThre.removeAt(index);
      if (index == wordsListThre.length) {
        lastIndex--;
        saveData3();
      }
      if (wordsListThre.isEmpty) {
        Navigator.pop(context);
      } else {
        saveData3();
        notifyListeners();
      }
    }
  }

  void resetList3() {
    wordsListThre.clear(); // Mevcut listeyi sıfırla

    // Başlangıç verilerini başlangıç listesi ile güncelle
    wordsListThre.addAll(initialList3);

    saveData3();
    notifyListeners(); // Değişiklikleri bildir
  }

